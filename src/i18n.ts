export type LocalizedTextValue =
  | string
  | string[]
  | {
      en?: string | string[];
      ja?: string | string[];
      vi?: string | string[];
      [lang: string]: string | string[] | undefined;
    }
  | null
  | undefined;

export const getText = (value: LocalizedTextValue, lang: string): string | string[] => {
  if (Array.isArray(value)) return value;
  if (typeof value === "string") return value;
  return value?.[lang] || value?.vi || value?.en || "";
};
