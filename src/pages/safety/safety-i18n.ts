export function translateSafetyText(lang: HubLanguage, value: string) {
  if (lang === "vi") return value;

  const source = String(value ?? "");
  const leading = source.match(/^\s*/)?.[0] || "";
  const trailing = source.match(/\s*$/)?.[0] || "";
  const normalized = source.trim().replace(/\s+/g, " ");
  if (!normalized) return source;

  const dictionary = lang === "ja" ? TEXT_JA : TEXT_EN;
  const exact = dictionary[normalized];
  if (exact) return `${leading}${exact}${trailing}`;

  for (const item of PATTERNS) {
    const match = normalized.match(item.pattern);
    if (match) {
      const translator = lang === "ja" ? item.ja : item.en;
      return `${leading}${translator(...match.slice(1))}${trailing}`;
    }
  }

  return source;
}
